for cat in cat_list:
    #for rand in random_heads:
        with open(f"./SafetyHeadAttribution/exp_res/{cat}-mistral-search5-scale1e-5/sample_ships_ablated.jsonl", "r") as f:
            res = [json.loads(line) for line in f]
        print(cat)
        count = 0
        result_list = []
        for i in res:
            prompt, response = list(i.items())[0]
            result = pipe(response, max_length = 512, truncation = True)[0]
            result["prompt"] = prompt
            result["response"] = response
            result_list.append(result)
        llm_res = pd.DataFrame(result_list)

        with open(f"./SafetyHeadAttribution/exp_res/ships_judged/granite31B_{cat}_mistral_sample.json", "w+") as f:
            for _, i in llm_res.iterrows():
                prompt = i["prompt"]
                response = i["response"]


                inputs = tokenizer([base_prompt.format(prompt = prompt, response = response)],
                                   return_tensors="pt").to("cuda:0")
                generate_ids = model.generate(inputs.input_ids, attention_mask = inputs.attention_mask, max_new_tokens=128, pad_token_id=tokenizer.pad_token_id,
                                              temperature=0.01)
                output = tokenizer.batch_decode(generate_ids[:, inputs.input_ids.shape[1]:], skip_special_tokens=True,
                                                clean_up_tokenization_spaces=False)

                f.write(json.dumps(output) + '\n')
                print("number of responses analysied: ", count)
                count += 1

